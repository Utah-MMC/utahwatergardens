// Helper function to create URL-friendly city names
export const createCityUrl = (cityName) => {
  return cityName.toLowerCase()
    .replace(/\s+/g, '-')
    .replace(/[^a-z0-9-]/g, '');
};

// Helper function to get all cities as a flat array
export const getAllCities = () => {
  const allCities = [];
  Object.values(utahCitiesByCounty).forEach(countyCities => {
    countyCities.forEach(city => {
      allCities.push({
        name: city,
        url: createCityUrl(city),
        path: `/${createCityUrl(city)}`
      });
    });
  });
  return allCities;
};

// Helper function to get cities by county with paths
export const getCitiesByCountyWithPaths = () => {
  const result = {};
  Object.entries(utahCitiesByCounty).forEach(([county, cities]) => {
    result[county] = cities.map(city => ({
      name: city,
      path: `/${createCityUrl(city)}`
    }));
  });
  return result;
};

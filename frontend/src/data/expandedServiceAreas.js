// Generate all city names as a flat array
export const getAllCities = () => {
  const allCities = [];
  
  Object.values(expandedServiceAreas).forEach(state => {
    Object.values(state).forEach(county => {
      allCities.push(...county);
    });
  });
  
  // Remove duplicates and sort
  return [...new Set(allCities)].sort();
};

// Generate all service page combinations
export const getAllServicePageCombinations = () => {
  const cities = getAllCities();
  const combinations = [];
  
  cities.forEach(city => {
    serviceTypes.forEach(service => {
      combinations.push({
        city: city,
        service: service,
        slug: `${service}-${city.toLowerCase().replace(/\s+/g, '-')}`,
        filename: `${service.charAt(0).toUpperCase() + service.slice(1)}${city.replace(/\s+/g, '')}Page.js`
      });
    });
  });
  
  return combinations;
};
